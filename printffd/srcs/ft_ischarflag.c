#include "../includes/ft_printf.h"

int	ft_ischarflag(char c)
{
	if (c == 'o' || c == 'x' || c == 'X' || c == 'e' || c == 'E'
			|| c == 'f' || c == 'g' || c == 'G')
		return (1);
	else
		return (0);
}
