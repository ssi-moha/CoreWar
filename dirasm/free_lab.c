#include "../includes/ft_corewar.h"

int		free_lab(t_lab **lab)
{
	if (!*lab)
		return (0);
	while (*lab)
	{
		if ((*lab)->l)
		{
			free((*lab)->l);
			(*lab)->l = 0;	
		}
		free(*lab);
		*lab = (*lab)->next;
	}
	return (0);
}
